# Tipos de int e float
# int -> Número inteiro
# O tipo de int representa qualquer número
# positivo ou negativo. int sem sinal é considerado
# positivo.
print(11) #int
print(-11) #int
print(0)

# float -> Número com ponto flutuante 
# O tipo de float representa qualquer número
# positivo ou negativo com ponto flutuante.
# float sem sinal é considerado positivo.
print(1.1, 10.11) # a virgula é para inserir mais um argumento
print(0.0, -1.5) #float

# A função type mostra o tipo que o Python
# inferiu ao valor.
print(type('Alexandre'))
print(type(12))
print(type(1.2), type(-1.1), type(0.0))