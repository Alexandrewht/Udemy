# Operações condicionais
# if    /   elif        /   else
# se    /  se não       /   se não

entrada = input('Você quer "entrar" ou "sair"? ')

if entrada == 'entrar':
    print('Você entrou no sistema')
elif entrada == 'sair':
    print('Você saiu do sistema')
else:
    print('Digite entrar ou sair')